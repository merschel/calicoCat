%
% RGB = viridis(varargin)
%
% Provides the viridis colormap. See
% https://www.youtube.com/watch?v=xAoljeRJ3lU
%
% Autor: Steve Merschel
% Date: 15/11/2015

function RGB = viridis(varargin)

if nargin > 0
    n = varargin{1};
else
    n = 64; % default
end

x = linspace(0,1,n);

pR = 1.0e+03 * [-0.929415424393895;
            4.071103288208648;
            -7.221764923386645;
            6.562070535754460;
            -3.124638715219965;
            0.641840261737853;
            0.032003376655552;
            -0.032744255814306;
            0.002069272963893;
            0.000195687957638;
            0.000268799307284];
pG = 1.0e+02 * [ 0.303758508163528;
            -1.174652251869710;
            1.544183916143165;
            -0.277170332581372;
            -1.340809217067792;
            1.533167384406346;
            -0.760664665536100;
            0.200241031568065;
            -0.035255887595662;
            0.016241474646890;
            0.000024820155029];
pB = 1.0e+03 * [ -0.103645679681330;
            0.739145835399823;
            -1.948416621360526;
            2.614514502819573;
            -1.969600104895188;
            0.836447175607243;
            -0.186401799172228;
            0.019952182119044;
            -0.003775867451830;
            0.001602104451456;
            0.000328944488356];
        
yR = polyval(pR,x);
yG = polyval(pG,x);
yB = polyval(pB,x);

RGB = [yR',yG',yB'];
